#Tuple
t=8,7,1,2,3,4,5,1 #paranthesis is optional , it will create tuple if there is no brackets 
print(t)

#count function
c=t.count(1)
print(c)

#index function
res=t.index(4)
print(res)

#min function
mint=min(t)
print(mint)

#max function
maxt=max(t)
print(maxt)

#sort function
tsort=sorted(t)
print(tsort)


